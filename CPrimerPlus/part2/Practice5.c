#include <stdio.h>

void br();

void ic();

// 编写一个程序，生成以下输出：
// 除了main()以外，该程序还要调用两个自定义函数：一个名为br()，调用一次打印一次“Brazil, Russia”；另一个名为ic()，调用一次打印一次“India, China”。其他内容在main()函数中完成。
int main() {

    br();
    printf("\t");
    ic();
    printf("\n");
    ic();
    printf("\n");
    br();
    return 0;
}

void br() {
    printf("Brazil, Russia");
}

void ic() {
    printf("India, China");
}