#include <stdio.h>

// 通过试验（即编写带有此类问题的程序）观察系统如何处理整数上溢、浮点数上溢和浮点数下溢的情况。
int main() {
    int a = 2147483647;
    printf("%d %d %d", a, a+1, a+2);// 整数上溢
    // 2147483647 -2147483648 -2147483647
    float b = 3.4E38 * 100.0f;
    printf("\n%e", b);// 浮点数上溢
    // 1.#INF00e+000
    float c = (3.4E-38) / (100.0E100);
    printf("\n%e", c);// 浮点数下溢
    // 0.000000e+000
    return 0;
}