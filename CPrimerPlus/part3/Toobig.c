#include <stdio.h>

// 溢出行为是未定义的行为，C标准并未定义有符号类型的溢出规则。
// 以下描述的溢出行为比较有代表性，但是也可能会出现其他情况。
int main(void) {
    int i = 2147483747;
    unsigned int j = 4294967295;
    printf("%d %d %d\n", i, i+1, i+2);
    printf("%u %u %u\n", j, j+1, j+2);

    // -2147483549 -2147483548 -2147483547
    // 4294967295 0 1
    return 0;
}