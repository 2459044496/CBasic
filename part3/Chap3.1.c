// 3.1 语句与程序块
/*
 * 在x=0、i++或printf(...)这样的表达式之后加上一个分号(;)，它们就变成了语句。
 * 例如：
 * x = 0;
 * i++;
 * printf(...);
 * 在C语言中，分号是语句结束符，而Pascal等语言却把分号用作语句之间的分隔符。
 *
 * 用一对花括号"{"与"}"把一组声明和语句括在一起就构成了一个复合语句（也叫做程序块），
 * 复合语句在语法上等价于单条语句。函数体中被花括号括起来的语句便是明显一例。
 * if、else、while和for之后被花括号括起来的多条语句也是类似的例子。
 * 右花括号用于结束程序块，其后不需要分号。
 *
 * */
