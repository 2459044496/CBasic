// 3.2 if-else语句
/*
 * if-else语句用于条件判定。其语法如下：
 * if（表达式）
 *      语句1
 * else
 *      语句2
 * 其中else部分是可选的。该语句执行时，先计算表达式的值，如果其值为真（即表达式的值为非0），
 * 则执行语句1；如果其值为假（即表达式的值为0），并且该语句包含else部分，则执行语句2.
 *
 * 由于if语句只是简单测试表达式的数值，因此可以对某些代码的编写进行简化。最明显的例子是用如下写法
 *      if（表达式）
 * 来代替
 *      if（表达式 ！= 0）
 *
 * */
